    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String uri="";
        String nombre="";
        String ruta="";
        try {
            nombre=txtNombreArchivo.getText();
            ruta=txtRuta.getText();
            uri=ruta+nombre;
            if(Files.exists(Path.of(uri))){
                JOptionPane.showMessageDialog(null, "No se puede crear el archivo porque ya existe!");
            }
            else{
            Path path=Files.writeString(Path.of(uri), "", StandardOpenOption.CREATE_NEW);
            if(Files.exists(Path.of(uri))){
                lblMensaje.setText("Se creó el archivo \n"+nombre+" \nen la dirección \n"+ruta);
            }            
            }
        } catch (Exception e) {e.printStackTrace();}
    }//GEN-LAST:event_btnCrearActionPerformed

