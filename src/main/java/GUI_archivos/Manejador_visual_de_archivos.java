    private void btnBuscarOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarOrigenActionPerformed
        String origen="";
        JFileChooser jfc = new JFileChooser();
        int returnVol = jfc.showOpenDialog(this);
        if(returnVol == JFileChooser.APPROVE_OPTION){
            origen = jfc.getSelectedFile().getAbsolutePath();
            txtOrigen.setText(origen);
        }
        else{
            txtOrigen.setText("");
        }
    }//GEN-LAST:event_btnBuscarOrigenActionPerformed

    private void btnCopiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCopiarActionPerformed
        Path origen = Path.of(txtOrigen.getText());
        Path destino = Path.of(txtDestino.getText());
        try {
            Files.copy(origen, destino.resolve("copia_"+origen.getFileName()),StandardCopyOption.REPLACE_EXISTING);
            txtMensajeCopiar.setText("El archivo fue copiado con el nombre de copia_"+origen.getFileName());
        } catch (Exception e) { e.printStackTrace();}
    }//GEN-LAST:event_btnCopiarActionPerformed

